package kr.or.ddit.vo;

public class MypillVO {
	private String pill_no;
	private String pill_name;
	private String pill_count;
	private String pill_start;
	private String pill_end;
	private String pill_alerttime;
	private String pill_img;
	private String pill_color;
	private String mem_no;
	
	
	public String getPill_no() {
		return pill_no;
	}
	public void setPill_no(String pill_no) {
		this.pill_no = pill_no;
	}
	public String getPill_name() {
		return pill_name;
	}
	public void setPill_name(String pill_name) {
		this.pill_name = pill_name;
	}
	public String getPill_count() {
		return pill_count;
	}
	public void setPill_count(String pill_count) {
		this.pill_count = pill_count;
	}
	public String getPill_start() {
		return pill_start;
	}
	public void setPill_start(String pill_start) {
		this.pill_start = pill_start;
	}
	public String getPill_end() {
		return pill_end;
	}
	public void setPill_end(String pill_end) {
		this.pill_end = pill_end;
	}
	public String getPill_alerttime() {
		return pill_alerttime;
	}
	public void setPill_alerttime(String pill_alerttime) {
		this.pill_alerttime = pill_alerttime;
	}
	public String getPill_img() {
		return pill_img;
	}
	public void setPill_img(String pill_img) {
		this.pill_img = pill_img;
	}
	public String getPill_color() {
		return pill_color;
	}
	public void setPill_color(String pill_color) {
		this.pill_color = pill_color;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	
	
	
	
}
