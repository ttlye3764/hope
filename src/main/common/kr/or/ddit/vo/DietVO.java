package kr.or.ddit.vo;

public class DietVO {
	private String diet_no;
	private String diet_kcal;
	private String mem_no;
	
	public String getDiet_no() {
		return diet_no;
	}
	public void setDiet_no(String diet_no) {
		this.diet_no = diet_no;
	}
	public String getDiet_kcal() {
		return diet_kcal;
	}
	public void setDiet_kcal(String diet_kcal) {
		this.diet_kcal = diet_kcal;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	
	
}
