package kr.or.ddit.vo;

public class MyCarVO {
	String car_no;
	String car_kinds;
	String car_date;
	String car_oil;
	String mem_no;
	public String getCar_no() {
		return car_no;
	}
	public void setCar_no(String car_no) {
		this.car_no = car_no;
	}
	public String getCar_kinds() {
		return car_kinds;
	}
	public void setCar_kinds(String car_kinds) {
		this.car_kinds = car_kinds;
	}
	public String getCar_date() {
		return car_date;
	}
	public void setCar_date(String car_date) {
		this.car_date = car_date;
	}
	public String getCar_oil() {
		return car_oil;
	}
	public void setCar_oil(String car_oil) {
		this.car_oil = car_oil;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	
}
