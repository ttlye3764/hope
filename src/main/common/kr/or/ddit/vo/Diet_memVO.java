package kr.or.ddit.vo;

public class Diet_memVO {
	
	private String dm_no;
	private String height;
	private String dm_date;
	private String current_weight;
	private String purpose_weight; 
	private String bmi;
	private String day_kcal;
	private String mem_no;
	
	public String getDm_no() {
		return dm_no;
	}
	public void setDm_no(String dm_no) {
		this.dm_no = dm_no;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	
	public String getDm_date() {
		return dm_date;
	}
	public void setDm_date(String dm_date) {
		this.dm_date = dm_date;
	}
	public String getCurrent_weight() {
		return current_weight;
	}
	public void setCurrent_weight(String current_weight) {
		this.current_weight = current_weight;
	}
	public String getPurpose_weight() {
		return purpose_weight;
	}
	public void setPurpose_weight(String purpose_weight) {
		this.purpose_weight = purpose_weight;
	}
	public String getBmi() {
		return bmi;
	}
	public void setBmi(String bmi) {
		this.bmi = bmi;
	}
	public String getDay_kcal() {
		return day_kcal;
	}
	public void setDay_kcal(String day_kcal) {
		this.day_kcal = day_kcal;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	
	
}
