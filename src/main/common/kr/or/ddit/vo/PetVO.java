package kr.or.ddit.vo;

public class PetVO {
	String pet_no;
	String mem_no;
	String pb_no;
	String pet_name;
	String pet_age;
	String pet_start_date;
	String pet_eat_time;
	public String getPet_no() {
		return pet_no;
	}
	public void setPet_no(String pet_no) {
		this.pet_no = pet_no;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	public String getPb_no() {
		return pb_no;
	}
	public void setPb_no(String pb_no) {
		this.pb_no = pb_no;
	}
	public String getPet_name() {
		return pet_name;
	}
	public void setPet_name(String pet_name) {
		this.pet_name = pet_name;
	}
	public String getPet_age() {
		return pet_age;
	}
	public void setPet_age(String pet_age) {
		this.pet_age = pet_age;
	}
	public String getPet_start_date() {
		return pet_start_date;
	}
	public void setPet_start_date(String pet_start_date) {
		this.pet_start_date = pet_start_date;
	}
	public String getPet_eat_time() {
		return pet_eat_time;
	}
	public void setPet_eat_time(String pet_eat_time) {
		this.pet_eat_time = pet_eat_time;
	}
	
	
}
