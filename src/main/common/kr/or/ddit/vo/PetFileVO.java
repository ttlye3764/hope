package kr.or.ddit.vo;

public class PetFileVO {
	String pf_no;
	String pb_no;
	String pf_name;
	String pf_save_name;
	String pf_size;
	String pf_type;
	public String getPf_no() {
		return pf_no;
	}
	public void setPf_no(String pf_no) {
		this.pf_no = pf_no;
	}
	public String getPb_no() {
		return pb_no;
	}
	public void setPb_no(String pb_no) {
		this.pb_no = pb_no;
	}
	public String getPf_name() {
		return pf_name;
	}
	public void setPf_name(String pf_name) {
		this.pf_name = pf_name;
	}
	public String getPf_save_name() {
		return pf_save_name;
	}
	public void setPf_save_name(String pf_save_name) {
		this.pf_save_name = pf_save_name;
	}
	public String getPf_size() {
		return pf_size;
	}
	public void setPf_size(String pf_size) {
		this.pf_size = pf_size;
	}
	public String getPf_type() {
		return pf_type;
	}
	public void setPf_type(String pf_type) {
		this.pf_type = pf_type;
	}
}
