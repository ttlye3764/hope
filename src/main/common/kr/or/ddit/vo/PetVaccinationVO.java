package kr.or.ddit.vo;

public class PetVaccinationVO {
	String pv_no;
	String pet_no;
	String pv_name;
	String pv_vacc_day;
	String pv_future_vacc_day;
	public String getPv_no() {
		return pv_no;
	}
	public void setPv_no(String pv_no) {
		this.pv_no = pv_no;
	}
	public String getPet_no() {
		return pet_no;
	}
	public void setPet_no(String pet_no) {
		this.pet_no = pet_no;
	}
	public String getPv_name() {
		return pv_name;
	}
	public void setPv_name(String pv_name) {
		this.pv_name = pv_name;
	}
	public String getPv_vacc_day() {
		return pv_vacc_day;
	}
	public void setPv_vacc_day(String pv_vacc_day) {
		this.pv_vacc_day = pv_vacc_day;
	}
	public String getPv_future_vacc_day() {
		return pv_future_vacc_day;
	}
	public void setPv_future_vacc_day(String pv_future_vacc_day) {
		this.pv_future_vacc_day = pv_future_vacc_day;
	}
	
}
