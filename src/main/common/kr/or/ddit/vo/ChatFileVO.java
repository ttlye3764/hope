package kr.or.ddit.vo;

public class ChatFileVO {
	private String cf_no;
	private String mem_no;
	private String ch_no;
	private String cf_size;
	private String cf_name;
	private String cf_type;
	private String cf_save_name;
	
	public String getCf_no() {
		return cf_no;
	}
	public void setCf_no(String cf_no) {
		this.cf_no = cf_no;
	}
	public String getMem_no() {
		return mem_no;
	}
	public void setMem_no(String mem_no) {
		this.mem_no = mem_no;
	}
	public String getCh_no() {
		return ch_no;
	}
	public void setCh_no(String ch_no) {
		this.ch_no = ch_no;
	}
	public String getCf_size() {
		return cf_size;
	}
	public void setCf_size(String cf_size) {
		this.cf_size = cf_size;
	}
	public String getCf_name() {
		return cf_name;
	}
	public void setCf_name(String cf_name) {
		this.cf_name = cf_name;
	}
	public String getCf_type() {
		return cf_type;
	}
	public void setCf_type(String cf_type) {
		this.cf_type = cf_type;
	}
	public String getCf_save_name() {
		return cf_save_name;
	}
	public void setCf_save_name(String cf_save_name) {
		this.cf_save_name = cf_save_name;
	}
	
	
}
